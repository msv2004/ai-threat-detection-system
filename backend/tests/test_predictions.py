    assert report_resp.status_code == 200
    report_data = report_resp.json()
    
    assert report_data["total_samples"] == 5
    assert "number_of_threats" in report_data
    assert "threat_distribution" in report_data
    assert "average_confidence" in report_data
    
    # Check predictions output file exists on disk
    job_uuid = uuid.UUID(job_id)
    job_record = db_session.query(PredictionJob).filter(PredictionJob.id == job_uuid).first()
    assert os.path.exists(job_record.output_file_path)


