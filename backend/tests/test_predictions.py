def test_threat_incident_workflow(auth_headers, activated_model, db_session):
    # Force threat prediction
    payload = {
        "input_data": {"age": 30.0, "color": "blue"}
    }
    client.post("/api/v1/predict", json=payload, headers=auth_headers)
    
    # Retrieve threats
    threats_resp = client.get("/api/v1/threats", headers=auth_headers)
    assert threats_resp.status_code == 200
    threats = threats_resp.json()
    
    if len(threats) > 0:
        threat = threats[0]
        assert threat["resolution_status"] == "Open"
        
        # Update resolution status to Resolved
        update_resp = client.put(
            f"/api/v1/threats/{threat['id']}/status", 
            json={"resolution_status": "Resolved"}, 
            headers=auth_headers
        )
        assert update_resp.status_code == 200
        assert update_resp.json()["resolution_status"] == "Resolved"
